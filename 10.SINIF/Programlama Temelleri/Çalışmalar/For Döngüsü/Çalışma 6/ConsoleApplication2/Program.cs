using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;

namespace ConsoleApplication2
{
    class Program
    {
        static void Main(string[] args)
        {
            // F-M Arasındaki Harfleri Yazan Program.
            char i;
            for (i = 'f'; i <= 'm'; i++)
            {
                Console.Write(i);
            }

            Console.ReadKey();
        }
    }
}
